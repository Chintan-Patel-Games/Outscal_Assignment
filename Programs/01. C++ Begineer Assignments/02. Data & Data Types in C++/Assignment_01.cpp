#include <iostream>

using namespace std;

int main()
{
    // Assignment - 1 : Obtain the size of Data Types

    int i;
    float f;
    char c;
    double d;
    long e;
    bool b;
    string s;

    cout << "Size of int is : " << sizeof(i) << endl;
    cout << "Size of float is : " << sizeof(f) << endl;
    cout << "Size of char is : " << sizeof(c) << endl;
    cout << "Size of double is : " << sizeof(d) << endl;
    cout << "Size of long is : " << sizeof(e) << endl;
    cout << "Size of bool is : " << sizeof(b) << endl;
    cout << "Size of string is : " << sizeof(s) << endl;
}